SET ECHO ON
SET TIMING ON

-- 删除H_VARCHAR_MAX、P_VARCHAR_MAX、H_VARCHAR_2G、P_VARCHAR_2G表
DROP TABLE IF EXISTS H_VARCHAR_MAX;
DROP TABLE IF EXISTS P_VARCHAR_MAX;
DROP TABLE IF EXISTS H_VARCHAR_2G;
DROP TABLE IF EXISTS P_VARCHAR_2G;

-- 创建varchar(2147483648)列的hdfs表
CREATE TABLE H_VARCHAR_2G(TVARCHAR varchar(2147483648));

-- 创建varchar(2147483647)列的hdfs表
CREATE TABLE H_VARCHAR_MAX(TVARCHAR varchar(2147483647));

-- 创建varchar(2147483648)列的pallas表
CREATE TABLE P_VARCHAR_2G(TVARCHAR varchar(2147483648)) ENGINE PALLAS;

-- 创建varchar(2147483647)列的pallas表
CREATE TABLE P_VARCHAR_MAX(TVARCHAR varchar(2147483647)) ENGINE PALLAS;


